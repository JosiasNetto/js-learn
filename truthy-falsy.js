//Truthy
//Values that are translated to true when used in a boolean context
//Exemples:
if (true);
if ({});
if ([]);
if (42);
if ("foo");
if (new Date());
if (-42);
if (3.14);
if (-3.14);
if (Infinity);
if (-Infinity);

//Falsy
//Values that are translated to false when used in a boolean context
//Exemples:
if (false);
if (null);
if (undefined);
if (0);
if (-0);
if ('');